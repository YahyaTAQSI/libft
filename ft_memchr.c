#include "libft.h"

void	*ft_memchr(const void *str, int c, size_t n)
{
	size_t			i;
	unsigned char	*s;

	s = (unsigned char *)str;
	i = 0;
	while (i < n && n > 0)
	{
		if ((unsigned char)c == s[i])
			return (s + i);
		i++;
	}
	return (NULL);
}
